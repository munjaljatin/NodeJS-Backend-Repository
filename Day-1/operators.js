// Operators in JavaSript
// Artihmetic operators

let x = 10;
let y = 20;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

// For floor division in JavaScript
console.log(Math.floor(x / y));
console.log(parseInt(22 / 3));
