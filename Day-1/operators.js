// Assignment operators in JavaScript
x += 20; // shorthand assignment operators
y -= 10;
x *= 10;
x /= 2;
y %= 10
console.log(x);
console.log(y);

// Logical operators in JavaScript
// Logical AND => &&
console.log(true && false);
console.log(0 && 1);
console.log(1 && -0);
console.log(2 && 3);
console.log(-0 && false);

// Logical OR => ||
console.log(1 || false);
console.log(-0 || false);
console.log(10 || 33);
console.log(0 || true);

// Logical NOT => !
console.log(!true);
console.log(!1);
console.log(!-0);
console.log(!false);